package com.asmasyakirah.android_custom_listview;

public class Layer
{
    String name;
    int image;

    public Layer(String name, int image)
    {
        this.name = name;
        this.image = image;
    }

    public int getImage()
    {
        return image;
    }

    public String getName()
    {
        return name;
    }
}
